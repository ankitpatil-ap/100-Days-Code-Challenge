#Variable Swap: Write a program that swaps the values of two variables without using a temporary variable.
a = 10
b = 20
print("Value of a is: ",a)
print("Value of b is: ",b)
#output Value of a is 10
#output Value of b is 20

#swap logic without using temp variable

a,b = b,a

print("Value of a is: ",a)
